import asyncio
import edge_tts

def create_test_voice():
    PHISHING_TEXT = """
    안녕하세요. 서울중앙지방검찰청 첨단범죄수사부 김민수 수사관입니다. 본인 오오오 씨 맞으십니까?
    다름이 아니라, 현재 저희 검찰에서 대규모 명의도용 및 금융 사기 사건을 수사 중인데, 이 사건 현장에서 본인 명의로 개설된 대포통장이 두 개나 발견되어서 사실 확인차 연락드렸습니다.

    혹시 최근에 신분증을 분실하시거나 지갑을 잃어버리신 적 있으십니까? 없으시다고요?
    현재 주범인 일당을 검거해서 조사하는 과정에서, 본인 명의의 우리은행과 농협 계좌가 불법 도박 사이트 자금 세탁에 사용된 정황이 확실하게 포착되었습니다. 현재까지 확인된 피해 금액만 무려 4천만 원에 달합니다.

    본인이 이 범죄에 직접 가담한 것인지, 아니면 단순 개인정보 유출로 인한 피해자인지 확인하는 녹취 조사가 반드시 필요합니다.
    만약 수사에 비협조적이시거나 전화를 끊으실 경우, 전자금융거래법 위반 혐의로 피의자 신분으로 전환되어 당장 구속 수사를 받을 수도 있는 엄중한 상황입니다.

    주변에 다른 사람이 있습니까? 본 사건은 기밀 수사로 진행되고 있으므로 제3자에게 발설할 경우 공무집행방해죄가 추가될 수 있습니다. 조용한 곳으로 이동해 주시기 바랍니다.

    피해자 입증을 위해서는 현재 보유하고 계신 다른 모든 은행 계좌의 자산 보호 조치가 시급합니다. 추가적인 불법 대출이 발생할 위험이 매우 높기 때문에, 금융감독원과 연계된 국가 안전 계좌로 임시 이체를 하셔야 자산을 보호받을 수 있습니다.

    메모지 있으시면 지금 바로 적어보세요. 사건 번호 2026 고합 1054호입니다. 나중에 담당 검사님과 통화하실 때 이 사건 번호를 반드시 말씀하셔야 합니다.
    지금 바로 스마트폰 열어서 제가 불러드리는 보안 어플리케이션을 설치하시고, 가까운 은행 ATM기나 창구로 이동해 주십시오. 이동하시는 동안 통화는 절대 끊으시면 안 됩니다.
    """
    VOICE = "ko-KR-InJoonNeural"
    OUTPUT = "test_voice.wav"

    async def main():
        tts = edge_tts.Communicate(PHISHING_TEXT, VOICE, rate="+5%")
        await tts.save(OUTPUT)
        print(f"AI 테스트용 음성 파일이 생성되었습니다: {OUTPUT}")

    asyncio.run(main())
    return OUTPUT
